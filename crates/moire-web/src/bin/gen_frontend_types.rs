use facet_typescript::TypeScriptGenerator;
use std::fs;
use std::path::PathBuf;

fn main() -> Result<(), String> {
    let mut gen = TypeScriptGenerator::new();

    gen.add_type::<moire_types::ConnectionsResponse>();
    gen.add_type::<moire_types::ConnectedProcessInfo>();
    gen.add_type::<moire_types::TriggerCutResponse>();
    gen.add_type::<moire_types::CutStatusResponse>();
    gen.add_type::<moire_types::ApiError>();
    gen.add_type::<moire_types::SqlRequest>();
    gen.add_type::<moire_types::QueryRequest>();
    gen.add_type::<moire_types::SqlResponse>();
    gen.add_type::<moire_types::SnapshotCutResponse>();
    gen.add_type::<moire_types::SnapshotSymbolicationUpdate>();
    gen.add_type::<moire_types::RecordStartRequest>();
    gen.add_type::<moire_types::RecordCurrentResponse>();
    gen.add_type::<moire_types::RecordingImportBody>();

    let generated = gen.finish();
    let mut out = String::new();
    out.push_str("// @generated by `cargo run -p moire-web --bin gen_frontend_types`\n");
    out.push_str("// Do not edit by hand.\n\n");
    out.push_str(&generated);

    let out_path = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("..")
        .join("..")
        .join("frontend")
        .join("src")
        .join("api")
        .join("types.generated.ts");

    if let Some(dir) = out_path.parent() {
        fs::create_dir_all(dir)
            .map_err(|e| format!("failed to create output directory {}: {e}", dir.display()))?;
    }
    fs::write(&out_path, out)
        .map_err(|e| format!("failed to write {}: {e}", out_path.display()))?;

    println!("wrote {}", out_path.display());
    Ok(())
}
